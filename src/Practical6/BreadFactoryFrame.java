    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            PrintWriter pw = new PrintWriter(
                    new FileWriter("breadTxt.txt"));
            pw.println(breadFactory.getCostOfFlour());
            for (int i = 0;
                    i < breadFactory.getNumOfBreads();
                    i++) { //i<5
                Bread b = breadFactory.getBread(i);
                pw.println(b.getName() + ","
                        + b.getNoOfLoaves());
            }
            pw.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    e.getMessage(), "Error !",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnRetrieveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetrieveActionPerformed
        // TODO add your handling code here:
        try {
            BufferedReader br = new BufferedReader(
                    new FileReader("breadTxt.txt"));

            String t = br.readLine(); //read the first line
            breadFactory.setCostOfFlour(
                    Double.parseDouble(t));

            String[] temp;
            for (int i = 0; i < breadFactory.getNumOfBreads();
                    i++) {
                t = br.readLine();
                temp = t.split(",");
                Bread b = breadFactory.getBread(i);
                b.setNoOfLoaves(Integer.parseInt(temp[1]));
            }
            br.close();

            txtFlourCost.setText(
                    breadFactory.getCostOfFlour() + "");
//            txtPlain.setText(
//                    breadFactory.getBread(0).getNumOfLoaves()+"");
//
//            txtWholemeal.setText(String.valueOf(
//                  breadFactory.getBread(1).getNumOfLoaves()));
//            txtRaisin.setText(String.valueOf(
//                  breadFactory.getBread(2).getNumOfLoaves()));
//
//            txtBanana.setText(Integer.toString(
//                  breadFactory.getBread(3).getNumOfLoaves()));
//            txtCorn.setText(Integer.toString(
//                  breadFactory.getBread(4).getNumOfLoaves()));

            JTextField[] inputs = {txtPlain, txtWholemeal,
                txtRaisin, txtBanana, txtCorn};

            for (int i = 0; i < breadFactory.getNumOfBreads(); i++) {
                inputs[i].setText(""
                        + breadFactory.getBread(i).getNoOfLoaves());
            }

            displayCosts();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    e.getMessage(),
                    "Error !",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRetrieveActionPerformed

