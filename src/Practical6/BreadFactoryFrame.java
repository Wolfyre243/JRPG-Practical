    public void getNumberOfLoaves() {
        JTextField[] inputs = {txtPlain, txtWholemeal,
            txtRaisin, txtBanana, txtCorn};

        for (int i = 0; i < breadFactory.getNumOfBreads(); i++) {
            breadFactory.getBread(i).setNoOfLoaves(
                    Integer.parseInt(inputs[i].getText()));
        }
    }

    public void displayCosts() {
        //Display the results in the text area        

        String msg = "Type\tNumber of loaves\tTotal Cost\n";
        msg += "====\t================\t=============\n";

        for (int i = 0; i < breadFactory.getNumOfBreads();
                i++) { // i< 5
            Bread b = breadFactory.getBread(i);
            double costs = b.getNoOfLoaves()
                    * b.getAmtOfFlour()
                    * breadFactory.getCostOfFlour();
            msg += b.getName() + "\t" + b.getNoOfLoaves()
                    + "\t\t$ " + costs + "\n";
        }
        txtOutput.setText(msg);
    }

